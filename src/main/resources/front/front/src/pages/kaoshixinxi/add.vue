          <el-form-item :style='{"padding":"10px","margin":"0 0 10px 0","background":"linear-gradient(180deg, rgba(255,255,255,1) 0%, rgba(178,213,232,1) 100%)"}' label="考试地点" prop="kaoshididian">
            <el-input v-model="ruleForm.kaoshididian" 
                placeholder="考试地点" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px 0","background":"linear-gradient(180deg, rgba(255,255,255,1) 0%, rgba(178,213,232,1) 100%)"}' label="报名费用" prop="baomingfeiyong">
            <el-input v-model="ruleForm.baomingfeiyong" 
