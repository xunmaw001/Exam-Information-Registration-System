<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            kaoshibianhao : false,
            kemumingcheng : false,
            kaoshishijian : false,
            kaoshididian : false,
            baomingfeiyong : false,
            zhuyishixiang : false,
            jiaoshizhanghao : false,
            jiaoshixingming : false,
            fengmian : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          kaoshibianhao: '',
          kemumingcheng: '',
          kaoshishijian: '',
          kaoshididian: '',
          baomingfeiyong: '',
          zhuyishixiang: '',
          jiaoshizhanghao: '',
          jiaoshixingming: '',
          fengmian: '',
        },
        jiaoshizhanghaoOptions: [],
        rules: {
          kaoshibianhao: [
          ],
          kemumingcheng: [
          ],
          kaoshishijian: [
          ],
          kaoshididian: [
          ],
          baomingfeiyong: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          zhuyishixiang: [
          ],
          jiaoshizhanghao: [
          ],
          jiaoshixingming: [
          ],
          fengmian: [
          ],
        },
      };
    },
    computed: {



    },
    components: {
    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='kaoshibianhao'){
              this.ruleForm.kaoshibianhao = obj[o];
              this.ro.kaoshibianhao = true;
              continue;
            }
            if(o=='kemumingcheng'){
              this.ruleForm.kemumingcheng = obj[o];
              this.ro.kemumingcheng = true;
              continue;
            }
            if(o=='kaoshishijian'){
              this.ruleForm.kaoshishijian = obj[o];
              this.ro.kaoshishijian = true;
              continue;
            }
            if(o=='kaoshididian'){
              this.ruleForm.kaoshididian = obj[o];
              this.ro.kaoshididian = true;
              continue;
            }
            if(o=='baomingfeiyong'){
              this.ruleForm.baomingfeiyong = obj[o];
              this.ro.baomingfeiyong = true;
              continue;
            }
            if(o=='zhuyishixiang'){
              this.ruleForm.zhuyishixiang = obj[o];
              this.ro.zhuyishixiang = true;
              continue;
            }
            if(o=='jiaoshizhanghao'){
              this.ruleForm.jiaoshizhanghao = obj[o];
              this.ro.jiaoshizhanghao = true;
              continue;
            }
            if(o=='jiaoshixingming'){
              this.ruleForm.jiaoshixingming = obj[o];
              this.ro.jiaoshixingming = true;
              continue;
            }
            if(o=='fengmian'){
              this.ruleForm.fengmian = obj[o].split(",")[0];
              this.ro.fengmian = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/jiaoshi/jiaoshizhanghao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.jiaoshizhanghaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      jiaoshizhanghaoChange () {
        this.$http.get('follow/jiaoshi/jiaoshizhanghao?columnValue=' + this.ruleForm.jiaoshizhanghao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.jiaoshixingming){
              this.ruleForm.jiaoshixingming = res.data.data.jiaoshixingming
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('kaoshixinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('kaoshixinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('kaoshixinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('kaoshixinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      fengmianUploadChange(fileUrls) {
          this.ruleForm.fengmian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

