<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            zhunkaozhengbianhao : false,
            kaoshibianhao : false,
            kemumingcheng : false,
            kaoshididian : false,
            kaoshiriqi : false,
            xuehao : false,
            xueshengxingming : false,
            kaoshichengji : false,
            shijuan : false,
            jiaoshizhanghao : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          zhunkaozhengbianhao: '',
          kaoshibianhao: '',
          kemumingcheng: '',
          kaoshididian: '',
          kaoshiriqi: '',
          xuehao: '',
          xueshengxingming: '',
          kaoshichengji: '',
          shijuan: '',
          jiaoshizhanghao: '',
        },
        rules: {
          zhunkaozhengbianhao: [
          ],
          kaoshibianhao: [
          ],
          kemumingcheng: [
          ],
          kaoshididian: [
          ],
          kaoshiriqi: [
          ],
          xuehao: [
          ],
          xueshengxingming: [
          ],
          kaoshichengji: [
          ],
          shijuan: [
          ],
          jiaoshizhanghao: [
          ],
        },
      };
    },
    computed: {



    },
    components: {
    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='zhunkaozhengbianhao'){
              this.ruleForm.zhunkaozhengbianhao = obj[o];
              this.ro.zhunkaozhengbianhao = true;
              continue;
            }
            if(o=='kaoshibianhao'){
              this.ruleForm.kaoshibianhao = obj[o];
              this.ro.kaoshibianhao = true;
              continue;
            }
            if(o=='kemumingcheng'){
              this.ruleForm.kemumingcheng = obj[o];
              this.ro.kemumingcheng = true;
              continue;
            }
            if(o=='kaoshididian'){
              this.ruleForm.kaoshididian = obj[o];
              this.ro.kaoshididian = true;
              continue;
            }
            if(o=='kaoshiriqi'){
              this.ruleForm.kaoshiriqi = obj[o];
              this.ro.kaoshiriqi = true;
              continue;
            }
            if(o=='xuehao'){
              this.ruleForm.xuehao = obj[o];
              this.ro.xuehao = true;
              continue;
            }
            if(o=='xueshengxingming'){
              this.ruleForm.xueshengxingming = obj[o];
              this.ro.xueshengxingming = true;
              continue;
            }
            if(o=='kaoshichengji'){
              this.ruleForm.kaoshichengji = obj[o];
              this.ro.kaoshichengji = true;
              continue;
            }
            if(o=='shijuan'){
              this.ruleForm.shijuan = obj[o];
              this.ro.shijuan = true;
              continue;
            }
            if(o=='jiaoshizhanghao'){
              this.ruleForm.jiaoshizhanghao = obj[o];
              this.ro.jiaoshizhanghao = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('kaoshichengji/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('kaoshichengji/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('kaoshichengji/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('kaoshichengji/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      shijuanUploadChange(fileUrls) {
          this.ruleForm.shijuan = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

